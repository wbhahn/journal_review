  ## Conclusion
  
  -   **FLAVOUR trial은 24개월의 관찰 결과, intermediate coronary stenosis 환자를 대상으로 PCI 시행 여부 결정 및 시행 시 치료 성적의 측면에서 FFR이 IVUS에 비해 열등하지 않음을 보였다.**<br><br>
  -   
  ## Discussion_1
  
  -   두 군의 병변 차이가 없었음에도, **IVUS군에서 PCI를 더 많이 수행했다.** <br><br>
  -   그러나 치료 성적에 차이가 없었으므로, 이는 현재 **IVUS의 PCI 시행 기준이 엄격하지 않다**는 점을 시사한다. <br><br>
  -   또한 협착이 flow-limiting 하지 않을 경우, 즉 **실제 허혈로 이어질 가능성이 낮은 경우에는 스텐트를 설치하는 것이 오히려 해로울 수 있다.** 따라서 IVUS 사용에 유의해야 한다.

## Discussion_2

-   반면 FFR은 스텐트 설치에 큰 도움이 되지 않는다고 여겨졌으나, 본 연구에서는 IVUS에 열등하지 않은 치료 성적을 보였다. <br><br>
  -   이는 **FFR이 스텐트 설치에도 도움이 될 수 있음을 시사한다.** 그 외에도 적은 PCI 사용은 **적은 항혈소판제 사용**으로 이어지는 또 다른 이점이 있다. <br><br>
  
  ## Discussion_3
  -   FFR과 IVUS는 각각 목적이 다르지만, 많은 기관이 한 기기만 구입하여 사용한다. <br><br>
  -   그러나 **FFR은 고위험군에서 치료를 덜 할 위험이 있고, IVUS에서 발견되는 심한 협착 및 고위험 plaque은 실제 예후와 연관이 없는 경우가 있다.** 따라서 두 검사법을 비교한 연구가 필요했다. <br><br>
  -   결론적으로, 본 연구결과는 **FFR과 IVUS 중 한 검사법만 사용하여도 괜찮음**을 시사한다.

## Limits_1

-   **저위험군 환자만 연구에 포함**하여 고위험군 환자에게 결과를 적용할 수 없다. <br><br>
  -   시술자는 intervention의 종류를 안 채로 시술을 시행했다 **(open-label)**.<br><br>
  -   임상시험이 **동양아시아 인종**에 대해서만 이뤄졌다.

## Limits_2

-   두 군의 **PCI 시행 기준이 서로 달랐고, plaque의 성상을 고려하지 않았다.**<br><br>
  -   **FFR과 IVUS외에도 생리적 기능평가, 혈관내 영상검사가 존재**하므로 다양한 검사법의 추가 비교가 필요하다. <br><br>
  -   본 연구는 **FFR과 IVUS를 모두 사용한 군**을 포함하지 않았으므로, 후속 연구가 필요하다.

