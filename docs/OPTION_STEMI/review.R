  # Results
  
  ## 등록 및 시술
  
  
  - 추적완료 99% (1년)
- 단계군 **단계 시술까지 3일(2–4)**에 완료 → **퇴원 전 완료**
  - 교차: 즉시→단계 9%, 단계→즉시 3%
  - 비범인 병변 PCI 시행: 즉시 63%, 단계 55%
  
  
  ## Primary endpoint (1년, ITT)
  
  
  - 즉시 65명(13%) vs 단계 53명(11%) → **HR 1.24 (95% CI 0.86–1.79)**, **비열등성 미충족**
  - 개별 구성요소(전체 사망, 비치명적 MI, 임의 재관류) **유의차 없음**
  ## Baseline characteristic
  
  -   **모집 대상**: 1682명 (2016/07 \~ 2019/08) <br><br>
  -   **분석 대상**: FFR 군 838명, Angiography 군 844명 <br><br>
  -   평균 나이 65세, 당뇨 환자 554명 (32.9%)

## Procedural characteristic

-   두 군의 **병변 길이, 혈관 직경, 협착 직경에는 유의미한 차이가 없었다.**<br><br>
  -   환자별 분석, 혈관별 분석 모두에서 **IVUS 군의 PCI 시행빈도가 더 높았다.** (환자 수: 65.3% vs 44.4%; 혈관 수: 58.4% vs 33.2%) <br><br>
  -   사용한 스텐트의 수와 총 길이에는 유의미한 차이가 없었다.

