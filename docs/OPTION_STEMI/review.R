## Glossary_1
  
  -   **Coronary artery disease(CAD)** : 관상동맥질환; 관상동맥이 막히거나 수축하여 심장으로의 혈액 공급이 어려운 상태
  -   **STEMI**: ST분절 상승 심근경색


## Glossary_2

-   **Revascularization** : 재혈관화; 혈류를 복원해주는 치료. 관상동맥의 경우, 시술과(PCI = Ballooning/Stenting) 수술(CABG) 모두 가능하다. <br><br>
  - **완전 재관류(Complete revascularisation)**: 범인혈관(culprit) 외 **비범인(non‑culprit) 병변**까지 PCI로 치료
  -   **Percutaneous Coronary Intervention(PCI)** : 경피적 관상동맥 중재술; 피부를 통해 도관을 삽입하여 막힌 관상동맥을 열어주는 시술. 일회성으로 풍선을 부풀려 확장하거나, 영구적으로 스텐트를 삽입한다. **본 연구에서는 스텐트 삽입만을 PCI로 간주했다.**
    
  ## Glossary_3
  
  -   **Coronary Angiography(CAG)** : 관상동맥 조영술; 관상동맥에 조영제를 넣어 구조 및 형태를 관찰하는 검사.<br><br>
  -   **Fractional Flow Reserve(FFR)** : 분획 혈류 예비력; 혈관확장제 투여 후 관상동맥 병변 전후 압력의 비율로 혈류 감소량을 추정하는 생리적 기능 검사.<br><br>
  - **Killip class**: 심부전 중증도 지표 (I: 없음, II–III: 급성 심부전)

## Background_1

-   **CAG는 CAD를 평가하고 PCI를 보조하는 (Guide) 표준 검사다.** CAG 상 협착이 70% 이상이라면 PCI를 시행하고, 40% 이하라면 약물 치료를 시행한다.<br><br>
  -   반면 **협착이 40\~70%로 애매한 경우에는 (intermediate stenosis)** CAG상 협착의 중증도가 허혈 유발 가능성과 연관이 크지 않고, 따라서 PCI 시행 여부를 결정하기 어렵다. 또한 CAG로는 스텐트가 잘 설치되었는지 알 방법이 없다.

## Background_2

-   **FFR**은 **실제 허혈 유발 가능성**을 잘 예측하지만 스텐트 설치에는 큰 도움이 안 된다. 반면 **IVUS**는 **스텐트 설치**에는 유용하지만 실제 허혈 가능성이 높은지 평가할 때는 큰 도움이 안 된다. <br><br>
  -   FFR과 IVUS는 각각 CAG만 시행했을 때보다 치료 성적이 좋은 것으로 밝혀졌지만, **원리가 다른 두 검사를 직접 비교한 (head-to-head) 연구가 없는 실정이다.**
  
  ## Background_3
  
  -   따라서 저자들은 **intermediate stenosis**를 가진 환자들에서 **FFR-guided PCI strategy가 IVUS-guided PCI strategy에 비하여 열등하지 않음**을 검증하기 위해 **FLAVOUR** trial을 시행하였다.<br><br> (본래 FFR의 우월성을 검증하려 했으나, 임상시험을 시작한 해에 IVUS의 성적이 좋다는 연구결과들이 출판되어 비열등성 검정으로 연구계획을 수정함. **FFR이 의료 자원을 덜 소비하므로 비열등성 검정 역시 유의미함**)

# Method

## Study design

-   Investigator-initiated, multicentre, open-label, non-inferiority, randomised controlled trial <br><br>
  -   한국의 14개 병원에서 시행

## Patients

-   **선정기준**: **STEMI and multivessel coronary artery disease** 진단, **Successful PCI** 받은은 19세 이상 환자들 <br><br>
  -   **Successful PCI**: Less than 20% on angigography, Grade 2 or 3 flow (Thrombolysis in Myocardial Infarction grading system) <br><br>
  -   **Multivessel Coronary Artery Disease**: at least one non-culprit lesion 직경 2.5mm 이상, 육안상 50% 이상상 stenosis <br><br>
  -   **제외기준**: 선행 PCI 이전 12시간 이상 ischaemic symptom onset  <br><br>
  
  ## Intervention_1
  
  -   Immediate Group, Staged Group **1:1 랜덤 배정** <br><br>
  - 즉시군: **초치료 시** 비범인 병변까지 PCI
  - 단계군: **입원 중 다른 날** 비범인 PCI (**중앙 3일, IQR 2–4일**)
  - **FFR**: 50–69% 협착에서 평가(≥70%는 영상평가로 PCI 결정)
  - 권고 스텐트: 3세대 everolimus‑eluting (Synergy) 등

