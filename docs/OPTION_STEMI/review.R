  ## Primary End point
  
  -   1년 복합 사건 — **Death**, **Non-fatal Myocardial Infarction**(자연/시술 관련), **Unplanned Revascularisation**(재관류)
  
  ## Secondary End point
  
  -   Primary end point의 구성요소들, 사용한 스텐트 수, 뇌졸중 유무, 환자 자가설문, CAG 기반 CAD 평가 도구 (SYNTAX score, QCA) 등
  -   Primary end point 와 동일 구성요소 (시기: 랜덤 배정 이후 1, 6개월)

## Follow up

-   랜덤 배정 후 1, 6, 12개월

## Sample size

-   사건률 가정 10.5% 
-   Noninferiority Margin HR 1.42
-   검정력 80%
-   단측검정 1종 오류 확률 2.5%
-   **최종 994명**
  
  ## Statistical analysis_1
  
  -   범주형 변수 -\> Chi-square test <br><br>
  -   연속변수 -\> T-test <br><br>
  -   Primary endpoint -\> Log rank test, Cox proportional-hazards regression <br><br>
  
  ## Statistical analysis_2
  
  -   **Intention-to-treat (ITT)** (per-protocol 분석도 시행) <br><br>
  -   Multiple comparison 보정, Multiplicity 교정은 계획하지 않음. <br><br>
  -   Endpoint 결측치는 제거하고, 민감도 분석으로 결측치 영향을 확인함.

